    private void btgapokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btgapokActionPerformed
       String nama = txtnama.getText();
       if (nama.isEmpty()){
           JOptionPane.showMessageDialog(this,"Nama Harus diisi","Peringatan",JOptionPane.WARNING_MESSAGE);
       }
       
       if(!rbnikah.isSelected()&!rbtidaknikah.isSelected()){
           JOptionPane.showMessageDialog(this, "Status anda belum dipilih", "Pesan Kesalahan",JOptionPane.ERROR_MESSAGE);
       }
       else if (cbgolongan.getSelectedIndex()==0){
           JOptionPane.showMessageDialog(this,"Golongan belum dipilih", "Pesan Kesalahan", JOptionPane.ERROR_MESSAGE);
       }
        int gaji = (int)cbgolongan.getSelectedIndex();
        
        switch (gaji) {
            case 1 : if (rbnikah.isSelected())
                        txtgapok.setText("2600000");
                     else
                        txtgapok.setText("2500000");
            break;
            case 2 : if (rbnikah.isSelected())
                        txtgapok.setText("4150000");
                     else
                        txtgapok.setText("4000000");
            break;
            case 3 : if (rbnikah.isSelected())
                        txtgapok.setText("7200000");
                     else
                        txtgapok.setText("7000000");
            break;
            case 4 : if (rbnikah.isSelected())
                        txtgapok.setText("10000000");
                     else
                        txtgapok.setText("9750000");
            break;
            
        }
        
        
    }//GEN-LAST:event_btgapokActionPerformed

