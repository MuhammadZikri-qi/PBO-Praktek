    private void btlemburActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btlemburActionPerformed
       
        if(!rbya.isSelected()&!rbtidaklembur.isSelected()){
           JOptionPane.showMessageDialog(this, "Status lembur anda belum dipilih", "Pesan Kesalahan",JOptionPane.ERROR_MESSAGE);
           return;
        }
        
        if (rbya.isSelected()){
           if (cblembur.getSelectedIndex()==0){
           JOptionPane.showMessageDialog(this,"Jam lembur belum dipilih", "Pesan Kesalahan", JOptionPane.ERROR_MESSAGE);  
           return;
           }
            
        }
        
        try{
        double gaji = Double.parseDouble(txtgapok.getText());
        if (gaji <=0){
            JOptionPane.showConfirmDialog(this, "Jumlah Gaji Pokok Harus Besar Dari Nol","Pesan error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (rbya.isSelected()){
        int lembur = (int) cblembur.getSelectedIndex();
        switch (lembur){
            case 1 : if (rbya.isSelected())
                     txtlembur.setText("25000");
                     else
                     txtlembur.setText("0");
            break;
            case 2 : if (rbya.isSelected())
                     txtlembur.setText("50000");
                     else
                     txtlembur.setText("0");
            break;
            case 3 : if (rbya.isSelected())
                     txtlembur.setText("75000");
                     else
                     txtlembur.setText("0");
            break;
        }
        } else if (rbtidaklembur.isSelected()){
            txtlembur.setText("0");
        }
        
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Gaji pokok Harus Angka Bulat", "Pesan Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btlemburActionPerformed

