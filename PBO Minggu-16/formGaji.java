    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (txtlembur.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Hitung gapok dan lembur terlebih dahulu!","Peringatan",JOptionPane.ERROR_MESSAGE);
                
        }
                 
      double gaji = Double.parseDouble(txtgapok.getText());
      double lembur = Double.parseDouble(txtlembur.getText());
      double total = gaji + lembur ;
      double pajak = total * 0.02;
      double bersih = total - pajak;
      
      txttotal.setText(String.valueOf((long) total));
      txtpajak.setText(String.valueOf((long) pajak));
      txtgajibersih.setText(String.valueOf((long) bersih));
        }catch (NumberFormatException e) {
            
        }
      
    }//GEN-LAST:event_jButton1ActionPerformed

